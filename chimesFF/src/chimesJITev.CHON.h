inline double Power(double X, int pow)
{
  switch (pow) {
  case 0: return 1.0 ;
  case 1: return X ;
  case 2: return X*X ;
  case 3: return X*X*X ;
  case 4: return X*X*X*X ;
  case 5: return X*X*X*X*X ;
  case 6: return X*X*X*X*X*X ;
  case 7: return X*X*X*X*X*X*X ;
  case 8: return X*X*X*X*X*X*X*X ;        
  default:
     return X * Power(X,pow-1) ;
  }
}

